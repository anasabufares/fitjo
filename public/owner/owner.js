/* =============================================================
   FitJo — Gym Owner dashboard
   A separate dashboard the gym owner reaches with an owner access
   code (generated by the super-admin). Manage members, coaches
   (access keys) and employees. All cloud actions are authorised by
   the owner code (x-owner-code header).
   ============================================================= */

const API = "/api/members";
let ownerCode = sessionStorage.getItem("fj_owner_code") || "";
let ownerName = "";
let oview = "members";
let members = null, coachCodes = null, employees = null;

const $ = (s, r = document) => r.querySelector(s);
const $$ = (s, r = document) => [...r.querySelectorAll(s)];
const esc = (s) => String(s ?? "").replace(/&/g, "&amp;").replace(/"/g, "&quot;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
const fmtWhen = (ts) => { try { return ts ? new Date(ts).toLocaleDateString(undefined, { year: "numeric", month: "short", day: "numeric" }) : "—"; } catch (e) { return "—"; } };

/* ---------- toast + theme ---------- */
let toastTimer;
function toast(msg, err = false) { const el = $("#atoast"); el.textContent = msg; el.classList.toggle("err", err); el.classList.add("show"); clearTimeout(toastTimer); toastTimer = setTimeout(() => el.classList.remove("show"), 2600); }
function applyTheme() { const t = localStorage.getItem("fj_theme") || "light"; document.body.setAttribute("data-theme", t); document.body.setAttribute("data-accent", localStorage.getItem("fj_accent") || "green"); $("#themeBtn").textContent = t === "light" ? "🌙" : "☀️"; }
function toggleTheme() { localStorage.setItem("fj_theme", (localStorage.getItem("fj_theme") || "light") === "light" ? "dark" : "light"); applyTheme(); }

/* ---------- cloud ---------- */
async function ownerAction(payload, extraHeaders) {
  const r = await fetch(API, { method: "POST", headers: { "Content-Type": "application/json", "x-owner-code": ownerCode, ...(extraHeaders || {}) }, body: JSON.stringify(payload) });
  if (r.status === 401) { relock("Wrong owner access code."); return null; }
  return r.json().catch(() => ({}));
}

/* ---------- gate ---------- */
function initGate() {
  if (ownerCode) return validateAndOpen(ownerCode);
  const go = () => { const v = $("#gatePass").value.trim(); if (!v) { $("#gateErr").textContent = "Enter your owner access code."; return; } validateAndOpen(v); };
  $("#gateBtn").addEventListener("click", go);
  $("#gatePass").addEventListener("keydown", (e) => { if (e.key === "Enter") go(); });
  $("#gatePass").focus();
}
async function validateAndOpen(code) {
  try {
    const r = await fetch(API, { method: "POST", headers: { "Content-Type": "application/json", "x-owner-code": code }, body: JSON.stringify({ action: "list" }) });
    if (r.status === 401) { $("#gateErr").textContent = "Wrong owner access code."; sessionStorage.removeItem("fj_owner_code"); return; }
    const j = await r.json().catch(() => ({}));
    if (!Array.isArray(j.members)) { $("#gateErr").textContent = j.error || "Couldn't reach the server."; return; }
    ownerCode = code; sessionStorage.setItem("fj_owner_code", code);
    ownerName = j.owner || ""; members = j.members;
    unlock();
  } catch (e) { $("#gateErr").textContent = "Couldn't reach the server (deploy on Netlify)."; }
}
function unlock() { $("#gate").style.display = "none"; $("#dash").style.display = "block"; startDashboard(); }
function relock(msg) { ownerCode = ""; sessionStorage.removeItem("fj_owner_code"); $("#dash").style.display = "none"; $("#gate").style.display = "flex"; $("#gateErr").textContent = msg || ""; $("#gatePass").value = ""; $("#gatePass").focus(); }

/* ---------- views ---------- */
function switchView(v) { oview = v; $$(".atab").forEach(b => b.classList.toggle("active", b.dataset.oview === v)); render(); }
function render() {
  if (oview === "members") return renderMembers();
  if (oview === "coaches") return renderCoaches();
  if (oview === "employees") return renderEmployees();
}

/* ---- Members ---- */
function renderMembers() {
  const arr = (members || []).slice().sort((a, b) => (b.createdAt || 0) - (a.createdAt || 0));
  $("#oBody").innerHTML = `
    <div class="admin-head"><h1>Members</h1><span class="count">${arr.length} member${arr.length === 1 ? "" : "s"}</span>
      <span class="live-dot" title="auto-updating">● live</span>
      <span class="admin-spacer"></span><button class="abtn sm" id="oAddMember">＋ Add member</button></div>
    <div class="cc-gen" id="oMemberForm" style="display:none;flex-wrap:wrap">
      <input class="member-search" id="omName" placeholder="Full name">
      <input class="member-search" id="omEmail" placeholder="Email">
      <input class="member-search" id="omPhone" placeholder="Phone">
      <button class="abtn" id="oSaveMember">Save</button>
      <button class="abtn ghost" id="oCancelMember">Cancel</button>
    </div>
    <div class="member-list">
      ${arr.length ? arr.map(m => `
        <div class="member-row">
          <div class="member-avatar">${esc((m.name || m.email || "?").trim().charAt(0).toUpperCase())}</div>
          <div class="member-info">
            <div class="nm">${esc(m.name || "(no name)")}</div>
            <div class="sub">${esc(m.email)}</div>
            <div class="member-meta">
              <span class="tag">📱 ${esc(m.phone || "—")}</span>
              ${m.goal ? `<span class="tag">🎯 ${esc(m.goal)}</span>` : ""}
              <span class="tag">${m.hasPlan ? "📋 plan ✓" : "📋 no plan"}</span>
              <span class="tag">🪙 ${esc(m.points ?? 0)}</span>
            </div>
          </div>
          <button class="abtn danger sm" data-delm="${esc(m.email)}">Remove</button>
        </div>`).join("") : `<div class="muted-note">No members yet.</div>`}
    </div>`;
  $("#oAddMember").onclick = () => { $("#oMemberForm").style.display = "flex"; $("#omName").focus(); };
  $("#oCancelMember").onclick = () => { $("#oMemberForm").style.display = "none"; };
  $("#oSaveMember").onclick = saveMember;
  $$("[data-delm]").forEach(b => b.onclick = () => removeMember(b.dataset.delm));
}
async function saveMember() {
  const email = $("#omEmail").value.trim().toLowerCase();
  if (!email || !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) { toast("Enter a valid email", true); return; }
  try {
    const r = await fetch(API, { method: "POST", headers: { "Content-Type": "application/json" }, body: JSON.stringify({ name: $("#omName").value.trim(), email, phone: $("#omPhone").value.trim() }) });
    const j = await r.json().catch(() => ({}));
    if (r.ok && j.ok) { const l = await ownerAction({ action: "list" }); if (l) { members = l.members; renderMembers(); } toast("Member added"); }
    else toast("Failed: " + (j.error || "error"), true);
  } catch (e) { toast("Cloud not reachable", true); }
}
async function removeMember(email) {
  if (!confirm(`Remove member ${email}?`)) return;
  const j = await ownerAction({ action: "delete", email });
  if (j && j.ok) { members = (members || []).filter(m => m.email !== email); renderMembers(); toast("Member removed"); }
}

/* ---- Coaches (access keys) ---- */
async function renderCoaches() {
  $("#oBody").innerHTML = `
    <div class="admin-head"><h1>Coach access keys</h1><span class="count" id="ocCount"></span></div>
    <div class="banner" style="border-color:var(--border);background:var(--surface-2);color:var(--muted)">Coaches sign in via <b>Gym staff → Coach</b> with the key to see members who allowed trainer contact.</div>
    <div class="cc-gen"><input class="member-search" id="ocLabel" placeholder="Coach name (optional)"><button class="abtn" id="ocGen">＋ Generate access key</button></div>
    <div class="coach-codes" id="ocList"><div class="muted-note">Loading…</div></div>`;
  $("#ocGen").onclick = genCoach;
  if (coachCodes === null) { const j = await ownerAction({ action: "coach-codes" }); if (j) coachCodes = j.codes || []; }
  drawCoachCodes();
}
function drawCoachCodes() {
  const el = $("#ocList"); if (!el) return;
  const arr = (coachCodes || []).slice().sort((a, b) => (b.createdAt || 0) - (a.createdAt || 0));
  $("#ocCount").textContent = `${arr.length} key${arr.length === 1 ? "" : "s"}`;
  el.innerHTML = arr.length ? arr.map(c => `
    <div class="cc-row"><div class="cc-info"><div class="cc-code">${esc(c.code)}</div><div class="cc-label">${esc(c.label || "Coach")} · ${fmtWhen(c.createdAt)}</div></div>
      <div class="acts"><button class="abtn ghost sm" data-copy="${esc(c.code)}">Copy</button><button class="abtn danger sm" data-delc="${esc(c.code)}">Revoke</button></div></div>`).join("")
    : `<div class="muted-note">No access keys yet.</div>`;
  $$("[data-copy]", el).forEach(b => b.onclick = () => { try { navigator.clipboard.writeText(b.dataset.copy); toast("Copied " + b.dataset.copy); } catch (e) { toast(b.dataset.copy); } });
  $$("[data-delc]", el).forEach(b => b.onclick = async () => { if (!confirm(`Revoke ${b.dataset.delc}?`)) return; const j = await ownerAction({ action: "coach-code-del", code: b.dataset.delc }); if (j && j.ok) { coachCodes = j.codes; drawCoachCodes(); toast("Revoked"); } });
}
async function genCoach() {
  const j = await ownerAction({ action: "coach-code-add", label: $("#ocLabel").value.trim() });
  if (j && j.ok) { coachCodes = j.codes; $("#ocLabel").value = ""; drawCoachCodes(); toast(`Access key: ${j.code}`); }
  else if (j) toast("Failed: " + (j.error || "error"), true);
}

/* ---- Employees ---- */
async function renderEmployees() {
  $("#oBody").innerHTML = `
    <div class="admin-head"><h1>Employees</h1><span class="count" id="oeCount"></span></div>
    <div class="cc-gen" style="flex-wrap:wrap">
      <input class="member-search" id="oeName" placeholder="Name">
      <input class="member-search" id="oeRole" placeholder="Role (e.g. Receptionist)">
      <input class="member-search" id="oePhone" placeholder="Phone">
      <button class="abtn" id="oeAdd">＋ Add employee</button>
    </div>
    <div class="member-list" id="oeList"><div class="muted-note">Loading…</div></div>`;
  $("#oeAdd").onclick = addEmployee;
  if (employees === null) { const j = await ownerAction({ action: "employees" }); if (j) employees = j.employees || []; }
  drawEmployees();
}
function drawEmployees() {
  const el = $("#oeList"); if (!el) return;
  const arr = (employees || []).slice().sort((a, b) => (b.createdAt || 0) - (a.createdAt || 0));
  $("#oeCount").textContent = `${arr.length} employee${arr.length === 1 ? "" : "s"}`;
  el.innerHTML = arr.length ? arr.map(e => `
    <div class="member-row"><div class="member-avatar">${esc((e.name || "?").charAt(0).toUpperCase())}</div>
      <div class="member-info"><div class="nm">${esc(e.name)}</div><div class="sub">${esc(e.role || "Staff")}</div>
        <div class="member-meta"><span class="tag">📱 ${esc(e.phone || "—")}</span><span class="tag">🗓️ ${fmtWhen(e.createdAt)}</span></div></div>
      <button class="abtn danger sm" data-dele="${esc(e.id)}">Remove</button></div>`).join("")
    : `<div class="muted-note">No employees yet. Add your gym's staff above.</div>`;
  $$("[data-dele]", el).forEach(b => b.onclick = async () => { if (!confirm("Remove this employee?")) return; const j = await ownerAction({ action: "employee-del", id: b.dataset.dele }); if (j && j.ok) { employees = j.employees; drawEmployees(); toast("Removed"); } });
}
async function addEmployee() {
  const name = $("#oeName").value.trim();
  if (!name) { toast("Enter a name", true); return; }
  const j = await ownerAction({ action: "employee-add", name, role: $("#oeRole").value.trim(), phone: $("#oePhone").value.trim() });
  if (j && j.ok) { employees = j.employees; ["oeName", "oeRole", "oePhone"].forEach(id => $("#" + id).value = ""); drawEmployees(); toast("Employee added"); }
  else if (j) toast("Failed: " + (j.error || "error"), true);
}

/* ---------- live auto-refresh (~15s + on focus) ---------- */
async function ownerPoll() {
  if (document.visibilityState !== "visible" || !ownerCode) return;
  const ae = document.activeElement;
  if (ae && ae.tagName === "INPUT" && $("#oBody") && $("#oBody").contains(ae)) return; // don't clobber typing
  try {
    if (oview === "members") {
      const f = $("#oMemberForm"); if (f && f.style.display !== "none") return;
      const j = await ownerAction({ action: "list" });
      if (j && Array.isArray(j.members) && JSON.stringify(j.members) !== JSON.stringify(members)) { members = j.members; renderMembers(); }
    } else if (oview === "coaches") {
      const j = await ownerAction({ action: "coach-codes" });
      if (j && Array.isArray(j.codes) && JSON.stringify(j.codes) !== JSON.stringify(coachCodes)) { coachCodes = j.codes; drawCoachCodes(); }
    } else if (oview === "employees") {
      const j = await ownerAction({ action: "employees" });
      if (j && Array.isArray(j.employees) && JSON.stringify(j.employees) !== JSON.stringify(employees)) { employees = j.employees; drawEmployees(); }
    }
  } catch (e) { /* ignore */ }
}

/* ---------- boot ---------- */
function startDashboard() {
  applyTheme();
  $("#ownerSub").textContent = ownerName ? "Gym owner · " + ownerName : "Gym owner";
  $("#themeBtn").onclick = toggleTheme;
  $("#signOutBtn").onclick = () => relock("");
  $$(".atab").forEach(b => b.onclick = () => switchView(b.dataset.oview));
  render();
  setInterval(ownerPoll, 15000);
  document.addEventListener("visibilitychange", () => { if (document.visibilityState === "visible") ownerPoll(); });
}
initGate();
